                    <h3 class="accordion-section active faqs-question"> <span>What is an eVisitor Visa?</span></h3>
                    <p class="faq-answer">
                        The eVisitor visa is commonly used as an Australian tourist visa that allows you to enter into Australia for a holiday or business purpose only. For example, visiting family and friends or undertaking business activities, such as attending a conference, making business enquiries, or for contractual negotiations. Business activity excludes selling goods or services to the Australian public.
                        <br/>An eVisitor visa is eligible for the citizens of the European Union countries who hold a valid passport of any of those countries.
                        <br/>
                        You will be authorised to travel to Australia once you have received a confirmation e-mail that your eVisitor visa is approved. As such, your details are stored in the immigration system and linked to your passport electronically. Therefore, it is accessible by airlines, travel agents and Australian border agencies

                    </p>

                    <h3 class="accordion-section faqs-question"> <span>eVisitor Visa Tourist - Subclass 651</span></h3>
                    <p class="faq-answer">
                        The eVisitor Visa is a travel visa that is available to European Union passport holders only, who wish to travel to Australia for tourism and/or for visiting friends and family.
                        <br/>
                        An eVisitor visa will allow you to stay in Australia for up to three months at a time within 12 months of its validity.
                        <br/>
                        To apply for eVisitor visa you must be located outside of Australia and hold a valid passport of one of the eligible countries. Further to this, you must meet some other pre-conditions at the time of your application.

                    </p>

                    <h3 class="accordion-section faqs-question"> <span>eVisitor Visa Business Subclass 651</span></h3>
                    <p class="faq-answer">
                        The eVisitor Visa allows individuals to visit Australia for business visiting purposes, such as, attending a conference, negotiating or exploring business opportunities etc.
                        <br/>
                        This visa will allow you to stay in Australia for up to three months at a time, within 12 months of its validity.
                        <br/>
                        To apply for business eVisitor visa you must be located outside of Australia and hold a valid passport of any eligible country. Other pre-conditions at the time of your application must also be met.

